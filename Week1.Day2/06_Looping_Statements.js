// for loop
for (let i = 0; i <= 10; i++) {
  console.log(i);
}

//for each
let automationTool = ["playwright", "selenium"];
automationTool.forEach(function (automation) {
  console.log(automation);
});

//while loop 1 to 10 in reverse order
let count = 10;
while (count) {
  console.log(count);

  count--;
}

//Do while
let num = 10;
do {
  console.log(num);
  num++;
} while (num <= 15);
