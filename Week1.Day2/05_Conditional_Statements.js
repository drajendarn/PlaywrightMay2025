//Nested If statement
let browserName = "Chrome";
function getBrowser() {
  if (browserName == "Chrome") {
    console.log("Chrome Browser");
  } else if (browserName == "edge") {
    console.log("edge Browser");
  }
}
getBrowser();

//switch statement
let browName = "Chrome";
function getBrowserName() {
  switch (browName) {
    case "Chrome":
      console.log("Chrome Browser");
    case "edge":
      console.log("Chrome Browser");
  }
}
getBrowserName();
